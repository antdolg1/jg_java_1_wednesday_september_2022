package teacher.lesson_5.lessoncode;

public class IncreaseDecreaseIntValue {

    public static void main(String[] args) {

        int number = 0;

        System.out.println("Number value is: " + number);

        number = number + 1;

        System.out.println("Number value is: " + number);

        number++;

        System.out.println("Number value is: " + number);

        number++;
        number++;
        number++;
        number++;

        System.out.println("Number value is: " + number);

        number--; //number = number - 1;

        System.out.println("Number value is: " + number);

    }
}
