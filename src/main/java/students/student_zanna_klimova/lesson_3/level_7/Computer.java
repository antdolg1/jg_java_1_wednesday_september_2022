package students.student_zanna_klimova.lesson_3.level_7;

public class Computer {
    String manufacturer;
    String model;

    Computer(String manufacturer, String model) {
        this.manufacturer = manufacturer;
        this.model = model;
    }

    String getManufacturer() {
        return manufacturer;
    }

    String getModel() {
        return model;
    }
}
