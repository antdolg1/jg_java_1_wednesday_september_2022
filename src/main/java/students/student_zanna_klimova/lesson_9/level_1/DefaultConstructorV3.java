public class DefaultConstructorV3 {
    private String fullName;
    private int age;
    private boolean male;
    private boolean female;

    public String getFullName() {
        return fullName;
    }

    public int getAge() {
        return age;
    }

    public boolean isMale() {
        return male;
    }

    public boolean isFemale() {
        return female;
    }
}
