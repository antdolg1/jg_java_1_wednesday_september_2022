class ArrayTask15 {
    public static void main(String[] args) {
        int[] numbers = new int[3];
        numbers[0] = 4;
        numbers[1] = 2;
        numbers[2] = 45;
        System.out.println(numbers[0]);
        System.out.println(numbers[1]);
        System.out.println(numbers[2]);

        numbers[0]+=2;
        numbers[1]+=2;
        numbers[2]+=2;
        System.out.println(numbers[0]);
        System.out.println(numbers[1]);
        System.out.println(numbers[2]);

    }

}
