package students.student_vladislav_saksin.lesson3.level7;

class Computer {

    String manufacturer;
    String model;

    Computer(String manufacturer, String model) {
        this.manufacturer = manufacturer;
        this.model = model;
    }
    String getManufacturer() {
        return this.manufacturer;
    }

    String getModel() {
        return this.model;
    }
}
